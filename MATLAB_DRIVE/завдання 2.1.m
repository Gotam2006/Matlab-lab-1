figure;
hold on;
grid on;

%% Точки класу 1
scatter3(P(1, T==1), P(2, T==1), P(3, T==1), ...
    80, 'r', 'filled');

%% Точки класу 0
scatter3(P(1, T==0), P(2, T==0), P(3, T==0), ...
    80, 'b', 'filled');

%% Параметри площини
w = net.IW{1};
b = net.b{1};

%% Створення сітки для площини
[x, y] = meshgrid(-3:0.5:3, -3:0.5:3);

%% Рівняння площини: w1*x + w2*y + w3*z + b = 0
z = -(w(1)*x + w(2)*y + b)/w(3);

%% Побудова площини
surf(x, y, z, 'FaceAlpha', 0.3, 'EdgeColor', 'none');

%% Підписи
xlabel('X1');
ylabel('X2');
zlabel('X3');
title('Класифікація тривимірних образів персептроном');

legend('Клас 1','Клас 0','Розділяюча площина');

view(3);
hold off;
