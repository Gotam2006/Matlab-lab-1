close all;

%% =========================
%% 1. НАВЧАЛЬНА ВИБІРКА
%% =========================

% X(1,:) - кількість пасажирів
% X(2,:) - вантажопідйомність

P = [ 2  3  4  5  6  7  8  3  4  5;
      200 300 450 600 700 800 900 400 550 1000];

% Вихід:
% 1 - легкові
% 0 - вантажні

T = [1 1 1 0 0 0 0 1 1 0];

%% =========================
%% 2. НОРМУВАННЯ (ВАЖЛИВО)
%% =========================
[Pn, ps] = mapminmax(P);

%% =========================
%% 3. СТВОРЕННЯ ПЕРСЕПТРОНА
%% =========================

net = perceptron;
net.trainFcn = 'trainc';   % використання адаптивного навчання
net.inputWeights{1,1}.learnFcn = 'learnpn';

%% =========================
%% 4. НАВЧАННЯ
%% =========================

net.trainParam.epochs = 100;

net = train(net, Pn, T);

%% =========================
%% 5. ПЕРЕВІРКА
%% =========================

Y = net(Pn);

disp('Результат класифікації:');
disp(Y);

%% =========================
%% 6. ГРАФІК РОЗДІЛЕННЯ
%% =========================

figure;
hold on;
grid on;

% точки класів
gscatter(Pn(1,:), Pn(2,:), T, 'rb', 'ox');

% межа рішення
w = net.IW{1};
b = net.b{1};

x = -1:0.01:1;
y = -(w(1)*x + b)/w(2);

plot(x, y, 'k', 'LineWidth', 2);

xlabel('Pasengers (normalized)');
ylabel('Load capacity (normalized)');
title('Розділення легкових і вантажних авто');
legend('Легкові','Вантажні','Розділяюча лінія');

hold off;